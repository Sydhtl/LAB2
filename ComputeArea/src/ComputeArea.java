
public class ComputeArea {

	public static void main(String[] args) {
		double radius = 50;
		double pi ;
		double area;
		 
		
		
		pi = 3.14159 ;
		
		area= radius * radius * pi;
		
		System.out.println("The area of the circle of the radius " + radius + " is " + area );
		
		

	}

}
